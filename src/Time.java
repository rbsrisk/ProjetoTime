
public class Time {

	private int Hora;
	private int Minuto;
	private int Segundo;
	
	
	public Time(int Hora, int Minuto, int Segundo) {
		
		this.Hora = Hora;
		this.Minuto = Minuto;
		this.Segundo = Segundo;
	}


	public int getHora() {
		return Hora;
	}


	public void setHora(int hora) {
		Hora = hora;
	}


	public int getMinuto() {
		return Minuto;
	}


	public void setMinuto(int minuto) {
		Minuto = minuto;
	}


	public int getSegundo() {
		return Segundo;
	}


	public void setSegundo(int segundo) {
		Segundo = segundo;
	}
	
	
	
	
	
}
